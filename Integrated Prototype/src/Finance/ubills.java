    public void autoincrement(){
      
    ArrayList<String> list = new ArrayList<String>();
        try {
            String sql = "SELECT Bill_ID as billid FROM Bill";
            
            pst=conn.prepareStatement(sql);
            
            rs = pst.executeQuery();
            while(rs.next())
            {
                list.add(rs.getString("billid"));
            }
            
            
            String a = list.get(list.size()-1);
           
            String ino[]=a.split("BL");
            
                
            int no=Integer.parseInt(ino[1]);
            no=no+1;
            bid.setText("BL"+no);
    
        } 
        catch (Exception e) {
            JOptionPane.showMessageDialog(null,e);
        }
        
        finally{
            try{
                pst.close();
                rs.close();
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(null,e);
            }
        }
}
